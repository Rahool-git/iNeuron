let temp = 60 // input

// to convert temperature in Fahrenheit to Celsius
temp_celsius = (temp - 32) * 5 /9
//console.log("Temperature value", temp "in Celsius is", temp_celsius, "C")
console.log(`Temperature value ${temp} in Celsius is ${temp_celsius} C`)

// to convert temperature in Celsius to Fahrenheit
temp_fahrenheit = temp * 9/5 +32
//console.log("Temperature value", temp, "in Fahrenheit is", temp_fahrenheit, "F")
console.log(`Temperature value ${temp} in Fahrenheit is ${temp_fahrenheit} F`)


